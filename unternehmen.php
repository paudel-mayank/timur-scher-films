  <div class="philosophie_text unternehmen garage">
    <h3 class="philosophie_heading">Über uns</h3>
    <p class="paragraph-5 unternehmen">Timur Scher Films ist ein Unternehmen, wo alle Leistungen „aus einer Hand“ im
      Bereich
      Fahrzeug für den Kunden abgearbeitet werden können. In diesem Bereich bieten wir, wie kein anderer, fast alle
      Dienstleistungen an und sind spezialisiert im Individualisieren von Fahrzeugen. Dabei versuchen wir alle Wünsche
      unserer Kunden zu erfüllen. Wir sind einer der bestmöglichen Ansprechpartner für sämtliche Anliegen rund ums
      KFZ. Mit unserer Idee möchten wir anderen Gewerbetreibenden die Möglichkeit bieten, sich in unserem Park weiter
      zu entfalten und einen Onlinehandel mit über 30.000 Produkten anzubieten. Uns ist es wichtig, unseren Kunden
      attraktive Designs zu schaffen und einen Ort zu bieten, an dem sie ihre Leidenschaft rund um ihr Fahrzeug
      ausleben. </p>
  </div>
  <div class="philosophie_text unternehmen garage_alt">
    <h3 class="philosophie_heading">Erfolgs-Geschichte</h3>
    <p class="paragraph-5 unternehmen">Das Unternehmen Timur Scher Films begann als eine Nebentätigkeit in hauseigener
      Garage mit dem Handel von Reifen und Felgen, geführt von zwei erfahrenen KFZ-Fachkräften. Binnen eines Jahres
      musste festgestellt werden, dass der Bedarf an Reifen und Rädern als Nebentätigkeit nicht mehr gestemmt werden
      kann ohne das Unternehmen zu erweitern. Somit ging der Weg nach Herzebrock-Clarholz an den
      „Herzebrocker-Waschpark“. Auf 120 m2 Fläche war es für den Start als Haupttätigkeit gut gerüstet. Kurz darauf
      wurde das Angebot der Folientechnik eingeführt, welche das Portfolio gut ergänzte und inzwischen ein nicht
      wegzudenkender Teil der Branche geworden ist. Auch hier musste nach einem Jahr festgestellt werden, dass der
      Platz nicht ausreichte, um beide Geschäftsbereiche sauber abzuarbeiten und dementsprechend ging der Weg nach
      Marienfeld, in erneut größere Räumlichkeiten. Aktuell bewirtschaftet Timur Scher Films eine Fläche von ca. 1500
      m2.
      Diese unterteilt sich in folgende Bereiche: Werkstatt, Lager, Kundenparkplätze, Showroom und Büros,
      Aufenthaltsräume und Sanitäranlagen. Es sind inzwischen acht Mitarbeiter bei uns beschäftigt: Drei im
      kaufmännischen Bereich und fünf in der Werkstatt. Wir sind ein Team mit Herz und Leidenschaft.</p>
